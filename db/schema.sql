  "referring_user_id" UUID,
  "referred_user_id" UUID
);

CREATE TABLE "currencies" (
  "id" INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "currency_code" varchar(3) UNIQUE
);

